package etf.santorini.hd150197d;

public class Koordinata {
	private int x;
	private int y;
	
	public Koordinata() {
		super();
	}
	public Koordinata(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}	
	
}
